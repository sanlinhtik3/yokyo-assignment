import { useSelector, useDispatch } from 'react-redux';
import { Link, NavLink, useNavigate } from 'react-router-dom';
import { useLogoutMutation } from '../slices/usersApiSlice';
import { logout } from '../slices/authSlice';
import ActiveLink from './ActiveLink';
import { useGetManageUserQuery } from '../slices/usersManageApiSlice';
import PendingStaus from './PendingStaus';
import UserStatus from './UserStatus';
import AdminStatus from './AdminStatus';
import PendingAlert from './PendingAlert';

const Header = () => {
  const { userInfo } = useSelector((state) => state.auth);
  const { data, isLoading } = useGetManageUserQuery(userInfo?._id);

  const dispatch = useDispatch();
  const navigate = useNavigate();

  const [logoutApiCall] = useLogoutMutation();

  const logoutHandler = async () => {
    try {
      await logoutApiCall().unwrap();
      dispatch(logout());
      navigate('/login');
    } catch (err) {
      console.error(err);
    }
  };

  console.log(userInfo === null)

  return (
    <>
      {userInfo && data?.access === 0 && (<PendingAlert />)}
      <nav className="bg-white border-gray-200">
        <div className="max-w-screen-xl flex flex-wrap items-center justify-between mx-auto p-4">
          <Link to="/" className="flex items-center">
            <img src="https://flowbite.com/docs/images/logo.svg" className="h-8 mr-3" alt="Flowbite Logo" />
            <h3 class="inline-block text-xl font-bold text-gray-900 dark:text-white md:text-3xl lg:text-4xl font-poppins"><span class="text-transparent bg-clip-text bg-gradient-to-r to-emerald-600 from-sky-400">YOKYO</span></h3>
            {/* <span className="self-center text-2xl font-semibold whitespace-nowrap dark:text-white">Flowbite</span> */}
          </Link>
          <div className="flex items-center md:order-2">

            {userInfo ? (
              <>

                {data?.access === 0 && <PendingStaus /> || data?.access === 1 && <UserStatus /> || data?.access === 2 && <AdminStatus />}


                <button type="button" className=' hidden lg:flex justify-center items-center bg-blue-500 text-pink-50 p-1.5 rounded-full text-xs mr-2' id="user-menu-button" aria-expanded="false" data-dropdown-toggle="user-dropdown" data-dropdown-placement="bottom">
                  <img className="w-8 h-8 rounded-full mr-1" src={`https://ui-avatars.com/api/?name=${userInfo.name}&background=bfdbfe&color=007bff`} alt="user photo" />
                  <span className=' text-bold mr-2'>{userInfo.name}</span>
                </button>

                <button type="button" className=" lg:hidden flex mr-3 text-sm bg-gray-800 rounded-full md:mr-0 focus:ring-4 focus:ring-gray-300 dark:focus:ring-gray-600" id="user-menu-button" aria-expanded="false" data-dropdown-toggle="user-dropdown" data-dropdown-placement="bottom">
                  <span className="sr-only">Open user menu</span>
                  <img className="w-8 h-8 rounded-full" src={`https://ui-avatars.com/api/?name=${userInfo.name}`} alt="user photo" />
                </button>



                <div className="z-50 hidden my-4 text-base list-none bg-white divide-y divide-gray-100 rounded-lg shadow dark:bg-gray-700 dark:divide-gray-600" id="user-dropdown">
                  <div className="px-4 py-3">
                    <span className="block text-sm text-gray-900 dark:text-white">{userInfo.name}</span>
                    <span className="block text-sm  text-gray-500 truncate dark:text-gray-400">{userInfo.email}</span>
                  </div>
                  <ul className="py-2" aria-labelledby="user-menu-button">
                    <li>
                      <Link to='/profile' className="block px-4 py-2 text-sm text-gray-700 hover:bg-gray-100 dark:hover:bg-gray-600 dark:text-gray-200 dark:hover:text-white">Profile</Link>
                    </li>
                    <li onClick={logoutHandler}>
                      <a href="#" className="block px-4 py-2 text-sm text-gray-700 hover:bg-gray-100 dark:hover:bg-gray-600 dark:text-gray-200 dark:hover:text-white">Sign out</a>
                    </li>
                  </ul>
                </div>

              </>
            ) : (
              <>
                <Link to={`/login`} type="button" className=" mr-2 text-white bg-blue-700 hover:bg-blue-800 focus:ring-4 focus:ring-blue-300 font-medium rounded-lg text-sm px-5 py-2.5 ">Login</Link>
                <Link to={`/register`} type="button" className="py-2.5 px-5 text-sm font-medium text-gray-900 focus:outline-none bg-white rounded-lg border border-gray-200 hover:bg-gray-100 hover:text-blue-700 focus:z-10 focus:ring-4 focus:ring-gray-200 ">Register</Link>
              </>
            )}
            <button data-collapse-toggle="navbar-user" type="button" className=" ml-2 inline-flex items-center p-2 w-10 h-10 justify-center text-sm text-gray-500 rounded-lg md:hidden hover:bg-gray-100 focus:outline-none focus:ring-2 focus:ring-gray-200 " aria-controls="navbar-user" aria-expanded="false">
              <span className="sr-only">Open main menu</span>
              <svg className="w-5 h-5" aria-hidden="true" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 17 14">
                <path stroke="currentColor" strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M1 1h15M1 7h15M1 13h15" />
              </svg>
            </button>
          </div>


          <div className="items-center justify-between hidden w-full md:flex md:w-auto md:order-1" id="navbar-user">
            <ul className="flex flex-col font-medium p-4 md:p-0 mt-4 border border-gray-100 rounded-lg bg-gray-50 md:flex-row md:space-x-8 md:mt-0 md:border-0 md:bg-white ">
              <li>
                <ActiveLink to="/">Home</ActiveLink>
              </li>
              {userInfo && data?.access !== 0 && (
                <li>
                  <ActiveLink to="video">Video</ActiveLink>
                </li>
              )}
              {data?.access === 2 && (
                <li>
                  <ActiveLink to="manage-users">Manage User</ActiveLink>
                </li>
              )}
              {userInfo && data?.access !== 0 && (
                <li>
                  <ActiveLink to="blog">Blog</ActiveLink>
                </li>
              )}
            </ul>
          </div>
        </div>
      </nav>
    </>

  )


};

export default Header;
